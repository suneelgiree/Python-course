friends = [1,30,5,23,4,5,6,7,8,9,10]

friends.append(11); # append() adds the element at the end of the list
friends.insert(1, 2); # insert() adds the element at the specified index
friends.remove(30); # remove() removes the element from the list
friends.sort(); # sort() sorts the list in ascending order
friends.reverse(); # reverse() reverses the list
friends.pop(); # pop() removes the last element from the list
friends.pop(1); # pop() removes the element at the specified index
print(friends);
